create table customer (
  age integer    not null,
  annualincome     integer    not null,
  callingnum      integer not null,
  customerid      integer not null,
  customersuspended integer not null,
  education varchar(300) not null,
  gender varchar(100) not null,
  homeowner varchar(100) not null,
  martialstatus varchar(100) not null,
  occupation varchar(300) not null,
  state    varchar(100)    not null
)